#include "3-calc.h"

/**
* op_add - Returns the sum of two integers
* @a: int
* @b: int
* Return: sum of the integers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Returns the difference of two integers
* @a: int
* @b: int
* Return: difference of the integers
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - Returns the product of two integers
* @a: int
* @b: int
* Return: product of the integers
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - Returns the result of the division of two integers
* @a: int
* @b: int
* Return: result of the division of the integers
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Returns the remainder of the division of two integers
* @a: int
* @b: int
* Return: remainder of the division of the integers
*/
int op_mod(int a, int b)
{
	return (a % b);
}